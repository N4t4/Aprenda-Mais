		<h1><span class="ap-logo"></span> Apreda Mais</h1>
	</header>

	<section>
		<p>
			Este trabalho apresentou a proposta de desenvolvimento de um sistema voltado para
			criação de conteúdos didáticos que auxiliem professores de ensinos fundamental, a tornar suas
			aulas mais interessantes e atrativas para os alunos. Este foram abordados conceitos importan-
			tes de como ferramentas desse tipo funcionam, e explorou as possibilidades de recursos que
			podem ser considerados no desenvolvimento destes instrumentos que ajudam a disseminar o
			conhecimento.
		</p>
		<p>
			Considerando o contexto atual é uma evidência que surgirão cada vez mais ferramentas
			com o mesmo propósito, isso devido a relevância e atenção do assunto da incorporação da
			informática na educação. Desenvolver estas ferramentas na internet (computação em nuvem),
			é um aspecto relevante, pois com essa abordagem o conteúdo é distribuído de maneira mais
			simples, atendendo a um público maior independente de distancias geográficas.
		</p>
		<p>
			Este trabalho apresentou uma ferramenta que permita o aluno ter mais participação no
			seu próprio ensino explorando um interesse natural que as crianças têm em relação as novas tec-
			nologia, mas para que ferramentas desse tipo realmente sejam efetivas quanto seus objetivos,
			elas devem ser desenvolvidas com grande compromisso e considerando principalmente o ponto
			de vista pedagógico ao criar uma intervenção no processo educacional. É importante ressaltar
			que o sistema proposto não busca substituir o papel do professor no seu trabalho de ensino,
			mas sim, disponibilizar recursos que o auxiliará na criação de seus artefatos educacionais, em
			contrapartida os professores devem estar preparados para integrar a informática no seus méto-
			dos, visto que o objetivo é melhorar continuamente a qualidade do que é produzido dentro das
			salas de aula. Para trabalhos futuros serão feitas pesquisas que levantem dados para avaliar se
			a utilização da ferramenta é realmente benéfica no processo de aprendizagem. Para realizar a
			pesquisa após o termino da implementação do sistema Aprenda Mais, o mesmo será testado em
			um grupo de professores e alunos voluntário, o teste levantará métricas que serão avaliadas para
			comprovar a eficiência do sistema.
		</p>
	</section>

